package Parameter;

public class Method_without_parameter_Using_static_Method {
	public static void main(String[] args) {
		
		abhishek();
		vaibhav();
		
	}
	
	public static void abhishek() {
		int a=20;
		int b=20;
		int c=20;
		int d=(a+b+c);
		System.out.println(d);
	}
	
	public static void vaibhav() {
		long a=30l;
		long b=30l;
		long c=30l;
		
		long sum=(a+b+c);
		System.out.println(sum);
		
	}

}
