package Methods;

public class Static_regular_method_call_from_same_class {
	public static void main(String[] args) {
		
		abhishek();
		vaibhav();
		satpal();
		
		
		
	} //main(-) close
	
	public static void abhishek() {
		System.out.println("abhishek is a boy. he lives in villege,he has 1 brother and 1 sister and he is a good boy");
	}

	public static void vaibhav() {
		System.out.println("vaibhav is a brother of ahishek");
	}
	
	public static void satpal() {
		System.out.println("satpal is a cousion of abhishek and vaibhav");
	}
		
	
	
}
